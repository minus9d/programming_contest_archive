class CheeseSlicing {
    public:
    int totalArea(int A, int B, int C, int S) {
        cout << endl;
        if (A % S == 0 || B % S == 0 || C % S == 0) {
            return (A * B * C / S);
        }
        
        int ans = 0;
        vector<int> lens{A, B, C};
        while(true) {
            sort(ALL(lens));
            // for(auto e: lens) cout << e << ",";
            // cout << endl;
            if (lens[0] < S) break;
            if (lens[2] < 2 * S) break;
            ans += lens[0] * lens[1];
            lens[2] -= S;
        }

        sort(ALL(lens));
        if (lens[0] >= S) {
            ans += lens[1] * lens[2];
        }

        return ans;
    }
};

