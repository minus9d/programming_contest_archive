class EllysSki {
    public:

    int func(vector<int> height) {
        int prev = -1;
        int ans = 0;
        int c = 0;
        for(auto h: height) {
            if (h >= prev) {
                ++c;
                ans = max(ans, c);
            } else {
                c = 1;
            }
            prev = h;
        }
        return ans;
    }

    int getMax(vector<int> height) {

        auto ans1 = func(height);
        reverse(ALL(height));
        auto ans2 = func(height);
        return max(ans1, ans2);
    }
};

