// 区間加算に対応しないBIT
// 1 originなので注意!!
class BitSimple
{
private:
    int m_n;
    vector<int> m_bit;
    
public:
    void init(int n) {
        m_n = n;
        m_bit.resize(n + 1);
    }

    // 要素1から要素iまでの和
    int sum(int i) {
        int s = 0;
        while (i > 0) {
            s += m_bit[i];
            i -= i & -i;
        }
        return s;
    }
    
    void add(int i, int x) {
        while (i <= m_n) {
            m_bit[i] += x;
            i += i & -i;
        }
    }
};

int main(void)
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    int N;
    cin >> N;

    vector<int> As(N);
    REP(n, N) {
        cin >> As[n];
        As[n]++;
    }

    BitSimple bit;
    bit.init(N);

    ll ans = 0;
    set<int> s;
    int seen = 0;
    for(auto a: As) {
        auto s = bit.sum(a);
        ans += seen - s;
        seen += 1;
        bit.add(a, 1);
    }

    REP(n, N) {
        cout << ans << endl;

        ans -= As[n] - 1;
        ans += (N - As[n]);
    }

    return 0;
}
