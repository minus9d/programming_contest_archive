long long modinv(long long a, long long m) {
    long long b = m, u = 1, v = 0;
    while (b) {
        long long t = a / b;
        a -= t * b; swap(a, b);
        u -= t * v; swap(u, v);
    }
    u %= m; 
    if (u < 0) u += m;
    return u;
}

int main(void)
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // (i*K) mod NがN-Sになる最小のiを求める
    int T; cin >> T;
    REP(t, T) {
        ll N, S, K;
        cin >> N >> S >> K;

        // 解説pdf通りに変数を置換
        // 以下、Ax ≡ B mod Mとなる最小のxを求める
        ll A = K;
        ll B = N - S;
        ll M = N;
        // cout << "N, S, K = " << N << "," << S << "," << K << endl;
        // cout << "A, B, M = " << A << "," << B << "," << M << endl;

        auto d = gcd(gcd(A, B), M);
        A /= d;
        B /= d;
        M /= d;
        
        if (gcd(A, M) != 1) {
            cout << -1 << endl;
        } else {
            // B * (Aの逆元) が答
            // Aの逆元は拡張ユークリッドの互除法で求める
            // 参考: https://qiita.com/drken/items/3b4fdf0a78e7a138cd9a
            cout << (B * modinv(A, M)) % M << endl;
        }
    }

    int N;
    cin >> N;

    return 0;
}
